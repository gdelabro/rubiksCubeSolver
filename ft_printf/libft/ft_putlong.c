#include "libft.h"

void	ft_putlong(long long n)
{
	if (n == -9223372036854775807)
		ft_putstr("-9223372036854775807");
	else if (n < 0 && (n *= -1))
		ft_putchar('-');
	if (n > 9)
		ft_putnbr(n / 10);
	ft_putchar(n % 10 + 48);
}
