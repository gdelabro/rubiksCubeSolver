#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	int i;
	int a;

	i = 0;
	a = 0;
	while (s1[i])
		i++;
	while (s2[a] && n)
	{
		s1[i + a] = s2[a];
		a++;
		n--;
	}
	s1[i + a] = 0;
	return (s1);
}
